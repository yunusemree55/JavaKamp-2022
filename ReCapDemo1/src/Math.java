
public class Math {
	
	int Toplama(int number1,int number2) {
		
		return number1 + number2;
		
		
	}
	
	int Cikarma(int number1,int number2) {
		
		if(number1 > number2) {
			return number1 - number2;
		}else {
			
			return number2 - number1;
		}
		
	}
	
	int Carpma(int number1,int number2) {
		
		return number1 * number2;
	}
	
	int Bolme(int number1,int number2) {
		
		return number1 / number2;
		
	}
	
	

}
